package edu.tongji.sse.ibm.pojo;

public class Edu_teacherInfo {
	
	private Integer id;
	private String email;
	private String name_ch;
	private String name_en;
	private String positions;
	private String courses;
	private String researchDir;
	private String picURL;
	private String info;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getName_ch() {
		return name_ch;
	}
	public void setName_ch(String name_ch) {
		this.name_ch = name_ch;
	}
	public String getName_en() {
		return name_en;
	}
	public void setName_en(String name_en) {
		this.name_en = name_en;
	}
	public String getPositions() {
		return positions;
	}
	public void setPositions(String positions) {
		this.positions = positions;
	}
	public String getCourses() {
		return courses;
	}
	public void setCourses(String courses) {
		this.courses = courses;
	}
	public String getResearchDir() {
		return researchDir;
	}
	public void setResearchDir(String researchDir) {
		this.researchDir = researchDir;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getPicURL() {
		return picURL;
	}
	public void setPicURL(String picURL) {
		this.picURL = picURL;
	}
}
